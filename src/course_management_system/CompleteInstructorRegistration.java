    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        txt_male.setActionCommand("Male");
        txt_female.setActionCommand("Female");
        conn = db.database_db();
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to Add Instructor?", "Create Account", JOptionPane.YES_NO_OPTION);
        if (opt == 0) {
            try {
                String user = txt_Uname.getText();
                String sql2 = "Select *from Instructor_Table where user_name=?";
                pst = conn.prepareStatement(sql2);
                pst.setString(1, user);
                ResultSet rs = pst.executeQuery();
                if (rs.isBeforeFirst()) {
                    rs.next();
                    JOptionPane.showMessageDialog(null, "Username " + txt_Uname.getText() + " Already Taken, Please choose another name", "Warning",
                            JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {

            }
            if (txt_Uname.getText().length() == 0 || txt_password.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "You must enter a username or password", "Warning",
                        JOptionPane.WARNING_MESSAGE);
            } else {

                try {
                    String fname = txt_fName.getText();
                    String sname = txt_Sname.getText();
                    String gender = genderButtonGroup.getSelection().getActionCommand();
                    String age = txt_age.getText();
                    String email = txt_email.getText();
                    String phone = txt_phone.getText();
                    String username = txt_Uname.getText();
                    String password = txt_password.getText();

                    String sql = "INSERT INTO Instructor_Table(first_name,second_name,age,gender,phone,email,user_name,password) VALUES (?,?,?,?,?,?,?,?) ";

                    pst = conn.prepareStatement(sql);
                    pst.setString(1, fname);
                    pst.setString(2, sname);
                    pst.setString(3, age);
                    pst.setString(4, gender);
                    pst.setString(5, phone);
                    pst.setString(6, email);
                    pst.setString(7, username);
                    pst.setString(8, password);

                    if (pst.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(this, "Instructor was successfully Added ");
                    }

                } catch (Exception e) {

                    JOptionPane.showMessageDialog(this, "Instructor was not added " + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

