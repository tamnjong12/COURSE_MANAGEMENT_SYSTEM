    private void txt_passwordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_passwordKeyTyped

        String upperCaseChars = "(.*[A-Z].*)";
        String lowerCaseChars = "(.*[a-z].*)";
        String numbers = "(.*[0-9].*)";
        String specialChars = "(.*[@,#,$,%].*$)";

        if (txt_password.getText().length() < 8
                || txt_password.getText().matches(upperCaseChars)
                || txt_password.getText().matches(lowerCaseChars)
                || txt_password.getText().matches(numbers)
                || txt_password.getText().matches(specialChars)) {
            warning_password.setText("Strong password");
            warning_password.setForeground(Color.BLUE);
            txt_password.setForeground(Color.BLUE);
        } else {
            warning_password.setText("make password Strong");
            warning_password.setForeground(Color.RED);
        }
        if (txt_password.getText().length() < 8) {
            warning_password.setText("weak password");
            warning_password.setForeground(Color.RED);
            txt_password.setForeground(Color.RED);
            Cgreaterthan8.setSelected(false);
        } else {
            Cgreaterthan8.setSelected(true);
        }

        if (!txt_password.getText().matches(upperCaseChars)) {
            warning_password.setText("weak password");
            warning_password.setForeground(Color.RED);
            txt_password.setForeground(Color.RED);
            Cuppercase.setSelected(false);
        } else {
            Cuppercase.setSelected(true);
        }

        if (!txt_password.getText().matches(lowerCaseChars)) {
            warning_password.setText("weak password");
            warning_password.setForeground(Color.RED);
            txt_password.setForeground(Color.RED);
            Clowercase.setSelected(false);
        } else {
            Clowercase.setSelected(true);
        }

        if (!txt_password.getText().matches(numbers)) {
            warning_password.setText("weak password");
            warning_password.setForeground(Color.RED);
            txt_password.setForeground(Color.RED);
            Cnumbers.setSelected(false);
        } else {
            Cnumbers.setSelected(true);
        }

        if (!txt_password.getText().matches(specialChars)) {
            warning_password.setText("weak password");
            warning_password.setForeground(Color.RED);
            txt_password.setForeground(Color.RED);
            Cspecialcharacter.setSelected(false);
        } else {
            Cspecialcharacter.setSelected(true);
        }
        if (txt_password.getText().isEmpty()) {
            warning_password.setText(null);
            txt_password.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_txt_passwordKeyTyped

