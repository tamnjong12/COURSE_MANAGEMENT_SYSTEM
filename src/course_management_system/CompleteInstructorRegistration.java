    private void txt_phoneKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_phoneKeyTyped
        if (txt_phone.getText().length() > 9) {
            evt.consume();
            txt_phone.setForeground(Color.RED);
            warning.setText("");
            warning.setForeground(Color.RED);
            warning.setText(warning.getText() + " Phone number cannot be greater than 9 digits");
        }
        if (txt_phone.getText().length() <= 9) {

            txt_phone.setForeground(Color.BLACK);
            warning.setText("");
        }
        String numbers = "(.*[0-9].*)";
        if (!txt_phone.getText().matches(numbers)) {
            warning.setText("Only Numbers");
            warning.setForeground(Color.RED);
            txt_phone.setForeground(Color.RED);

        } else {
            txt_phone.setForeground(Color.BLACK);
            warning.setForeground(Color.BLACK);
        }
        if (txt_phone.getText().isEmpty()) {
            warning.setText(null);
        }

    }//GEN-LAST:event_txt_phoneKeyTyped

