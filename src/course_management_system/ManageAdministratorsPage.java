    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        txt_male.setActionCommand("Male");
        txt_female.setActionCommand("Female");
        conn = db.database_db();
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to Add this Administrator?", "Create Account", JOptionPane.YES_NO_OPTION);
        if (opt == 0) {
            try {
                String fname = txt_fname.getText();
                String sname = txt_sname.getText();
                String gender = genderButtonGroup.getSelection().getActionCommand();
                String age = txt_age.getText();
                String email = txt_email.getText();
                String phone = txt_phone.getText();
                String password = txt_password.getText();

                String sql = "INSERT INTO Administrators_Table(first_name,second_name,gender,age,email,phone,password) VALUES (?,?,?,?,?,?,?) ";

                pst = conn.prepareStatement(sql);
                pst.setString(1, fname);
                pst.setString(2, sname);
                pst.setString(3, gender);
                pst.setString(4, age);
                pst.setString(5, email);
                pst.setString(6, phone);
                pst.setString(7, password);

                if (pst.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Administrator was successfully Added ");
                    updateTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Aministrator was not added " + e.getMessage());
            }
        }
    }//GEN-LAST:event_AddButtonActionPerformed

    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        conn = db.database_db();
        txt_male.setActionCommand("Male");
        txt_female.setActionCommand("Female");
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to Update this Administrators Data??", "Update Data", JOptionPane.YES_NO_OPTION);
        if (opt == 0) {
            try {
                int id = Integer.parseInt(txt_id.getText());
                String fname = txt_fname.getText();
                String sname = txt_sname.getText();
                String gender = genderButtonGroup.getSelection().getActionCommand();
                String age = txt_age.getText();
                String email = txt_email.getText();
                String phone = txt_phone.getText();
                String password = txt_password.getText();

                String sql = "Update  Administrators_Table SET first_name=?, second_name=?, gender=?, age=?, email=?, phone=?, password=? WHERE id=?";
                pst = conn.prepareStatement(sql);

              
                pst.setString(1, fname);
                pst.setString(2, sname);
                pst.setString(3, gender);
                pst.setString(4, age);
                pst.setString(5, email);
                pst.setString(6, phone);
                pst.setString(7, password);
                pst.setInt(8, id);

                if (pst.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(this, "Administrators data has been Updated  ");
                    updateTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Update failed  " + e.getMessage());
            }

        }

    }//GEN-LAST:event_UpdateButtonActionPerformed
