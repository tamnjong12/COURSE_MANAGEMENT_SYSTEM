    private void SeachButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeachButtonActionPerformed
        conn = db.database_db();
        try {
            int id = Integer.parseInt(txt_id.getText());
            String sql = "Select *from Administrators_Table where id=?";

            pst = conn.prepareStatement(sql);

            pst.setInt(1, id);

            ResultSet rs = pst.executeQuery();

            if (rs.isBeforeFirst()) {
                rs.next();
                txt_fname.setText(rs.getString(2));
                txt_sname.setText(rs.getString(3));
                String gender = (rs.getString(4));
                if (gender.equals("Male")) {
                    txt_male.setSelected(true);
                } else {
                    txt_female.setSelected(true);
                }
                txt_age.setText(rs.getString(5));
                txt_email.setText(rs.getString(6));
                txt_phone.setText(rs.getString(7));
                txt_password.setText(rs.getString(8));
                 
                  

            } else {
                JOptionPane.showMessageDialog(null, "Administrator not found it might have been deleted  ");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Search failed  " + e.getMessage());
        }
    }//GEN-LAST:event_SeachButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
         conn = db.database_db();
        int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this Student?", "Delete Student", JOptionPane.YES_NO_OPTION);
        if (opt == 0) {
            try {
                int id = Integer.parseInt(txt_id.getText());
                String sql = "DELETE from Administrators_Table where id=?";
                pst = conn.prepareStatement(sql);
                pst.setInt(1, id);
                if (pst.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Administrator has been Deleted  ");
                    clear();
                    updateTable();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Delete failed  " + e.getMessage());
            }
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

